#!/usr/bin/env node

/**
 * generate-client-catalog.mjs — Generates the slim, client-safe product catalog.
 *
 * The full catalog (src/data/products/*.ts, ~1.8MB of TS with complete
 * bilingual HTML descriptions) must never reach the browser. The cart,
 * however, needs catalog pricing/bundle facts (slug, sku, price, variants…)
 * on the client so localStorage carts refresh to the server-enforced price.
 *
 * This script loads every product module (they are dependency-free data
 * files; Node's built-in TypeScript type-stripping executes them directly)
 * and writes src/data/client-catalog.generated.ts containing ONLY the slim
 * fields — no descriptions, features, meta, FAQs, or expert opinions.
 *
 * Run:  node scripts/generate-client-catalog.mjs
 * Runs automatically before `npm run dev` and `npm run build`.
 * Re-run after adding/editing any file under src/data/products/.
 */

import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import { pathToFileURL, fileURLToPath } from 'url';
import { spawnSync } from 'child_process';

const ROOT = join(import.meta.dirname, '..');

// Node 22.6–22.17 supports TS imports only behind --experimental-strip-types
// (>=22.18 / >=23.6 enable it by default). If this Node can't import .ts,
// re-run ourselves once with the flag so the build never breaks on CI.
function respawnWithStripTypes() {
    const result = spawnSync(
        process.execPath,
        ['--experimental-strip-types', fileURLToPath(import.meta.url), ...process.argv.slice(2)],
        { stdio: 'inherit' },
    );
    process.exit(result.status ?? 1);
}
const BARREL = join(ROOT, 'src', 'data', 'seed-products.ts');
const OUTPUT = join(ROOT, 'src', 'data', 'client-catalog.generated.ts');

// ── Discover product modules from the barrel (keeps catalog order identical
//    to `products` in seed-products.ts, which drives server-side ordering). ──
const barrelSource = readFileSync(BARREL, 'utf8');
const moduleNames = [];
const importRe = /^import \{ ([A-Za-z0-9_]+) \} from '\.\/products\/([A-Za-z0-9_.-]+)';$/gm;
let m;
while ((m = importRe.exec(barrelSource)) !== null) {
    moduleNames.push({ exportName: m[1], file: m[2] });
}
if (moduleNames.length === 0) {
    console.error('❌ No product imports found in src/data/seed-products.ts');
    process.exit(1);
}

// ── Load each product module (plain data files, no imports of their own). ──
const products = [];
for (const { exportName, file } of moduleNames) {
    const url = pathToFileURL(join(ROOT, 'src', 'data', 'products', `${file}.ts`)).href;
    let mod;
    try {
        mod = await import(url);
    } catch (err) {
        if (err?.code === 'ERR_UNKNOWN_FILE_EXTENSION' && !process.execArgv.includes('--experimental-strip-types')) {
            respawnWithStripTypes();
        }
        throw err;
    }
    const product = mod[exportName];
    if (!product || typeof product !== 'object' || !product.slug) {
        console.error(`❌ ${file}.ts did not export a product object named ${exportName}`);
        process.exit(1);
    }
    products.push(product);
}

// ── Slim mapping: pricing/bundle/cart fields ONLY. No descriptions. ──
const slim = products.map(p => {
    const primary = (p.images || []).find(img => img.isPrimary) || (p.images || [])[0];
    const entry = {
        slug: p.slug,
        sku: p.sku,
        brand: p.brand,
        categorySlug: p.categorySlug,
        price: p.price,
        originalPrice: p.originalPrice,
        stock: p.stock,
        featured: !!p.featured,
        status: p.status,
        translations: {
            en: { name: p.translations?.en?.name || '' },
            ar: { name: p.translations?.ar?.name || '' },
        },
    };
    if (primary) entry.image = { url: primary.url, alt: primary.alt || '' };
    if (Array.isArray(p.variants) && p.variants.length > 0) {
        entry.variants = p.variants.map(v => ({
            id: v.id,
            sku: v.sku,
            price: v.price,
            stock: v.stock,
            ...(v.isDefault ? { isDefault: true } : {}),
        }));
    }
    return entry;
});

const header = `// AUTO-GENERATED by scripts/generate-client-catalog.mjs — DO NOT EDIT MANUALLY.
// Slim, description-free catalog that is safe to ship in client bundles.
// Source of truth: src/data/products/*.ts (via src/data/seed-products.ts).
// Regenerate: node scripts/generate-client-catalog.mjs

/** Slim variant — pricing facts only. */
export interface ClientCatalogVariant {
    id: string;
    sku: string;
    price: number;
    stock: number;
    isDefault?: boolean;
}

/** Slim product — pricing/bundle/cart fields only (never descriptions). */
export interface ClientCatalogProduct {
    slug: string;
    sku: string;
    brand: string;
    categorySlug: string;
    price: number;
    originalPrice: number;
    stock: number;
    featured: boolean;
    status: string;
    translations: { en: { name: string }; ar: { name: string } };
    image?: { url: string; alt: string };
    variants?: ClientCatalogVariant[];
}

export const clientCatalog: ClientCatalogProduct[] = `;

writeFileSync(OUTPUT, header + JSON.stringify(slim, null, 4) + ';\n', 'utf8');

const kb = (Buffer.byteLength(readFileSync(OUTPUT)) / 1024).toFixed(1);
console.log(`✅ client-catalog.generated.ts written — ${slim.length} products, ${kb}KB`);
