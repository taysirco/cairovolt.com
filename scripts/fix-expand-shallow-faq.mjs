let totalFixed = 0;

for (const file of readdirSync(DIR).filter(f => f.endsWith('.ts') && !f.startsWith('_'))) {
  const filePath = join(DIR, file);
  let content = readFileSync(filePath, 'utf-8');
  let changed = false;
  
  for (const [old, expanded] of Object.entries(expansions)) {
    if (content.includes(old)) {
      content = content.replace(old, expanded);
      console.log(`✅ ${file.replace('.ts','')}: Expanded "${old.substring(0,40)}..."`);
      changed = true;
      totalFixed++;
    }
  }
  
  if (changed) writeFileSync(filePath, content, 'utf-8');
}

console.log(`\n🎯 Expanded ${totalFixed} FAQ answers`);
