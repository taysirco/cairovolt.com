import { execFile } from 'node:child_process';
import { readdir, readFile, stat, writeFile } from 'node:fs/promises';
import { extname, relative, resolve, sep } from 'node:path';
import { fileURLToPath } from 'node:url';
import { promisify } from 'node:util';

const projectRoot = fileURLToPath(new URL('../../../', import.meta.url));
const publicDirectory = resolve(projectRoot, 'public');
const outputFile = resolve(
    projectRoot,
    'src/app/image-sitemap.xml/public-image-paths.generated.ts',
);
const imageExtensions = new Set([
    '.avif',
    '.gif',
    '.jpeg',
    '.jpg',
    '.png',
    '.svg',
    '.webp',
]);
const runCommand = promisify(execFile);

async function collectImagePaths(directory) {
    const entries = await readdir(directory, { withFileTypes: true });
    const paths = [];

    for (const entry of entries) {
        const entryPath = resolve(directory, entry.name);
        if (entry.isDirectory()) {
            paths.push(...await collectImagePaths(entryPath));
        } else if (entry.isFile() && imageExtensions.has(extname(entry.name).toLowerCase())) {
            const publicPath = `/${relative(publicDirectory, entryPath).split(sep).join('/')}`;
            paths.push(publicPath);
        }
    }

    return paths;
}

async function collectDeployableImagePaths() {
    try {
        const { stdout } = await runCommand(
            'git',
            ['ls-files', '-z', '--', 'public'],
            { cwd: projectRoot, encoding: 'utf8' },
        );
        const trackedPaths = stdout.split('\0').filter(Boolean);
        const imagePaths = [];

        for (const repositoryPath of trackedPaths) {
            if (!imageExtensions.has(extname(repositoryPath).toLowerCase())) continue;
            try {
                if (!(await stat(resolve(projectRoot, repositoryPath))).isFile()) continue;
            } catch {
                continue;
            }
            imagePaths.push(`/${repositoryPath.slice('public/'.length)}`);
        }

        return imagePaths;
    } catch {
        // Source archives may not include Git metadata; those builds contain
        // only deployable source files, so scanning public is equivalent.
        return collectImagePaths(publicDirectory);
    }
}

const imagePaths = (await collectDeployableImagePaths()).sort();
const fileContents = `// Generated by the image-sitemap prebuild helper.
// Do not edit by hand.
export const PUBLIC_IMAGE_PATHS = new Set<string>(${JSON.stringify(imagePaths, null, 4)});
`;

let currentContents = '';
try {
    currentContents = await readFile(outputFile, 'utf8');
} catch {
    // The first run creates the generated module.
}

if (currentContents !== fileContents) {
    await writeFile(outputFile, fileContents, 'utf8');
}

console.log(`Image sitemap asset manifest: ${imagePaths.length} files.`);
